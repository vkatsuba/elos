-module(elos_app).

%%% ============================================================================
%%% Behaviours
%%% ============================================================================

-behaviour(application).

%%% ============================================================================
%%% Exports of application
%%% ============================================================================

-export([start/2, stop/1]).

%%% ============================================================================
%%% Callbacks of application
%%% ============================================================================

